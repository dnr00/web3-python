tokenID = 4198
messages = [
    "OOGA MOOLA",
    "BULLAS ARE BACKED",
    "MOOOO!",
    "BULL ISH"
]
count = 0

def click():
    """click 트랜잭션을 수행"""
    contract = web3.eth.contract(address=CONTRACT_ADDRESS, abi=CONTRACT_ABI)

    nonce = web3.eth.get_transaction_count(YOUR_WALLET_ADDRESS)
    gas_price = web3.eth.gas_price
    message = random.choice(messages)

    transaction = contract.functions.click(tokenID, message).build_transaction({
        'chainId': 80084,
        'gas': 300000,
        'gasPrice': gas_price,
        'nonce': nonce,
        'value': web3.to_wei(0.04269, 'ether')
    })

    signed_txn = web3.eth.account.sign_transaction(transaction, private_key=YOUR_PRIVATE_KEY)
    tx_hash = web3.eth.send_raw_transaction(signed_txn.raw_transaction)
        
    logging.info(f"Transaction sent. Hash: 0x{tx_hash.hex()}")
        
    tx_receipt = web3.eth.wait_for_transaction_receipt(tx_hash, timeout=120)
        
    if tx_receipt['status'] == 1:
        logging.info("Transaction Status: Success")
    else:
        logging.warning("Transaction Status: Fail")

async def click_with_delay():
    """일정 간격으로 click 트랜잭션을 반복 실행"""
    global count
    while True:
        try:
            if count >= 50:
                logging.info("Reached 50 transactions. Resetting count and waiting 60 seconds...")
                count = 0
                await asyncio.sleep(60)  # 50회 완료 후 60초 대기
                continue
                
            click()
            logging.info("Waiting for next transaction...")
            await asyncio.sleep(1)  # 트랜잭션 간 1초 대기
            count += 1
            logging.info(f"Current count: {count}")
            
        except Exception as e:
            logging.error(f"Error occurred: {e}")
            await asyncio.sleep(1)

async def main():
    logging.info("Starting the script...")
    await click_with_delay()

if __name__ == '__main__':
    asyncio.run(main())